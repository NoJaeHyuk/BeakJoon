SELECT c.FLAVOR 
FROM (
SELECT 
a.FLAVOR,
sum(a.TOTAL_ORDER + b.TOTAL_ORDER1) as TOTAL_ORDER
FROM FIRST_HALF a
JOIN 
(
 SELECT sum(TOTAL_ORDER) as TOTAL_ORDER1, FLAVOR
 FROM JULY
 GROUP BY FLAVOR   
) b
ON a.FLAVOR = b.FLAVOR 
GROUP BY a.FLAVOR  
ORDER BY TOTAL_ORDER DESC
) c   
LIMIT 3

